#!/usr/bin/env node
/**
 * oneop CLI — the agent collaboration kit.
 *
 *   oneop list                          list known apps
 *   oneop creds <app> [--env dev]       dev logins (REDACTED by default)
 *                    [--reveal|--json] full values (dev only — stg/prod are pointers)
 *   oneop run <app> [--env dev]         run the app's start cmd, capturing logs
 *   oneop logs <app> [--errors]         read captured logs (you OR an agent)
 *                   [--since 5m] [--follow] [--json] [--lines N]
 */
import { spawn } from "node:child_process";
import {
  existsSync,
  readFileSync,
  writeFileSync,
  copyFileSync,
  mkdirSync,
  appendFileSync,
  symlinkSync,
} from "node:fs";
import os from "node:os";
import path from "node:path";
import readline from "node:readline";
import YAML from "yaml";
import { loadPlaybooks } from "../lib/playbooks";
import { planEnv, renderEnvBlock } from "../lib/env";
import { findProjects, detectProject, starterPlaybook } from "../lib/detect";
import type { Playbook } from "../lib/schema";
import {
  appendLogSync,
  detectLevel,
  logPath,
  parseSince,
  readLogs,
  type Level,
} from "../lib/logs";

type Flags = Record<string, string | boolean>;

function parseArgs(argv: string[]): { cmd?: string; positional: string[]; flags: Flags } {
  const [cmd, ...rest] = argv;
  const positional: string[] = [];
  const flags: Flags = {};
  for (let i = 0; i < rest.length; i++) {
    const a = rest[i];
    if (a.startsWith("--")) {
      const key = a.slice(2);
      const next = rest[i + 1];
      if (next && !next.startsWith("--")) {
        flags[key] = next;
        i++;
      } else flags[key] = true;
    } else positional.push(a);
  }
  return { cmd, positional, flags };
}

function die(msg: string): never {
  console.error(`oneop: ${msg}`);
  process.exit(1);
}

async function findApp(name: string): Promise<Playbook> {
  const { playbooks } = await loadPlaybooks();
  const pb = playbooks.find((p) => p.app === name);
  if (!pb) {
    const names = playbooks.map((p) => p.app).join(", ") || "(none found)";
    die(`unknown app "${name}". Known: ${names}`);
  }
  return pb;
}

function envName(flags: Flags): "dev" | "staging" | "prod" {
  const e = (flags.env as string) || "dev";
  if (e !== "dev" && e !== "staging" && e !== "prod") die(`--env must be dev|staging|prod`);
  return e;
}

/** Best-effort repo root for running commands: strip a trailing /.ops. */
function repoRoot(pb: Playbook): string {
  if (pb.repoRoot) return pb.repoRoot;
  const dir = pb._file ? path.dirname(pb._file) : process.cwd();
  return path.basename(dir) === ".ops" ? path.dirname(dir) : dir;
}

const HELP = `oneop — your dev keyring + agent collaboration kit

  oneop init  [dir] [--link] [--dry] [--exclude a,b] [--json]   scaffold playbooks for every project under dir
  oneop list
  oneop creds <app> [--env dev] [--reveal|--json]
  oneop env   <app> [--write] [--json]
  oneop run   <app> [--env dev]
  oneop logs  <app> [--errors] [--since 5m] [--follow] [--json] [--lines N]
`;

const STARTER_HEADER = `# oneop playbook — generated by \`oneop init\`. Pointers, never secrets.
# Fill the TODOs, mirror your seeded dev users under envs.dev.accounts.users,
# and put anything sensitive in playbook.local.yaml (gitignored, never committed).
# Full contract: https://oneop.dev  /  docs/SCHEMA.md
`;

function playbooksLinkDir(): string {
  return process.env.ONEOP_PLAYBOOKS_DIR || path.join(os.homedir(), "playbooks");
}

function ensureOverlayIgnored(dir: string) {
  const gi = path.join(dir, ".gitignore");
  const line = ".ops/*.local.yaml";
  const cur = existsSync(gi) ? readFileSync(gi, "utf8") : "";
  if (!cur.includes(line)) appendFileSync(gi, `${cur && !cur.endsWith("\n") ? "\n" : ""}\n# oneop: sensitive overlay, never commit\n${line}\n`);
}

/** Symlink a playbook into the link dir (idempotent). */
function linkPlaybook(opsFile: string, app: string, linkDir: string): boolean {
  mkdirSync(linkDir, { recursive: true });
  const target = path.join(linkDir, `${app}.playbook.yaml`);
  try {
    if (!existsSync(target)) symlinkSync(opsFile, target);
    return true;
  } catch {
    return false;
  }
}

/** The oneop repo itself — never scaffold into it. */
function selfRepoRoot(): string {
  return path.resolve(path.dirname(new URL(import.meta.url).pathname), "..");
}

async function cmdInit(dirArg: string | undefined, flags: Flags) {
  const root = path.resolve(dirArg || process.cwd());
  const projects = findProjects(root);
  if (!projects.length) die(`no projects found under ${root} (looked for package.json / .git in subdirs)`);

  const link = !!flags.link;
  const linkDir = playbooksLinkDir();
  const self = selfRepoRoot();
  const exclude = new Set(
    String(flags.exclude || "")
      .split(",")
      .map((s) => s.trim())
      .filter(Boolean)
  );
  const results: { app: string; status: string; detail: string }[] = [];

  for (const dir of projects) {
    // Never scaffold into oneop itself, or anything the user excluded.
    if (path.resolve(dir) === self || exclude.has(path.basename(dir))) {
      results.push({ app: path.basename(dir), status: "excluded", detail: "" });
      continue;
    }

    const opsFile = path.join(dir, ".ops", "playbook.yaml");
    const d = detectProject(dir);
    const exists = existsSync(opsFile);

    // Already onboarded: don't rewrite, but DO link it if asked (the missing-link bug).
    if (exists) {
      const linked = link && !flags.dry ? linkPlaybook(opsFile, d.app, linkDir) : false;
      results.push({
        app: d.app,
        status: link ? "exists→linked" : "skip",
        detail: linked ? "symlinked existing playbook" : "already has .ops/playbook.yaml",
      });
      continue;
    }

    const yamlStr = STARTER_HEADER + YAML.stringify(starterPlaybook(d));
    const detail = `${d.start ?? "no dev cmd"}${d.url ? "  " + d.url : ""}${d.seedFile ? "  seed:" + d.seedFile : ""}`;

    if (flags.dry) {
      results.push({ app: d.app, status: "would-create", detail });
      continue;
    }
    mkdirSync(path.join(dir, ".ops"), { recursive: true });
    writeFileSync(opsFile, yamlStr, "utf8");
    ensureOverlayIgnored(dir);
    if (link) linkPlaybook(opsFile, d.app, linkDir);
    results.push({ app: d.app, status: "created", detail });
  }

  if (flags.json) return out({ root, linkDir: link ? linkDir : null, results });
  console.log(`oneop init: scanned ${root} — ${projects.length} project(s)${flags.dry ? " (dry run)" : ""}\n`);
  for (const r of results) {
    const mark =
      r.status === "created" ? "✓" : r.status.startsWith("exists") ? "→" : r.status === "excluded" ? "✗" : r.status === "skip" ? "·" : "○";
    console.log(`  ${mark} ${r.app.padEnd(22)} ${r.status.padEnd(14)} ${r.detail}`);
  }
  if (!flags.dry) {
    console.log(`\nNext: fill the TODOs in each .ops/playbook.yaml, then point the dashboard at them:`);
    console.log(`  export ONEOP_PLAYBOOKS_DIR="${link ? linkDir : "$HOME/playbooks"}"  &&  pnpm dev`);
  }
}

async function cmdList() {
  const { playbooks, isExample, dir } = await loadPlaybooks();
  console.log(`${playbooks.length} app(s) from ${dir}${isExample ? " (example data)" : ""}:`);
  for (const p of playbooks) {
    const envs = Object.keys(p.envs).join(", ");
    console.log(`  ${p.app.padEnd(20)} ${p.description ?? ""}  [${envs}]`);
  }
}

async function cmdCreds(name: string, flags: Flags) {
  const pb = await findApp(name);
  const env = envName(flags);
  const acct = pb.envs[env]?.accounts;
  const json = !!flags.json;
  const reveal = !!flags.reveal || json;

  if (!acct) return json ? out({ app: name, env, accounts: null }) : console.log(`No accounts for ${name}/${env}.`);

  // The bright line: only dev seeded users carry values. stg/prod are pointers.
  if (acct.source === "vault") {
    const payload = { app: name, env, source: "vault", vaultItem: acct.vaultItem };
    if (json) return out(payload);
    return console.log(`🔐 ${name}/${env}: open 1Password item "${acct.vaultItem}" (oneop never stores the value)`);
  }
  if (acct.source === "inline-nonsecret") {
    if (json) return out({ app: name, env, source: "inline-nonsecret", note: acct.note });
    return console.log(`👤 ${name}/${env}: ${acct.note}`);
  }

  // seed (dev)
  const users = acct.users ?? [];
  if (json) return out({ app: name, env, source: "seed", seedFile: acct.seedFile, users });
  if (!users.length) return console.log(`🌱 ${name}/${env}: seeded — see ${acct.seedFile} (no users mirrored yet)`);
  console.log(`🌱 ${name}/${env} dev logins${reveal ? "" : "  (run with --reveal or --json for passwords)"}:`);
  for (const u of users) {
    const id = u.email ?? u.username ?? "?";
    const pw = reveal ? (u.password ?? u.note ?? "—") : "••••••••";
    console.log(`  ${id.padEnd(28)} ${pw.padEnd(16)} ${u.role ?? ""}`);
  }
}

function out(o: unknown) {
  console.log(JSON.stringify(o, null, 2));
}

async function cmdEnv(name: string, flags: Flags) {
  const pb = await findApp(name);
  const root = repoRoot(pb);
  const envPath = path.join(root, ".env");
  const examplePath = path.join(root, ".env.example");
  const existing = existsSync(envPath) ? readFileSync(envPath, "utf8") : "";
  const example = existsSync(examplePath) ? readFileSync(examplePath, "utf8") : "";

  const plan = planEnv(pb, existing, example);
  if (flags.json) return out(plan);

  console.log(`.env plan for ${name}  (${envPath})`);
  if (!plan.managed.length) console.log("  (no dependencies declared)");
  for (const m of plan.managed) {
    const tag = m.status === "new" ? "＋ new " : "= kept";
    console.log(`  ${tag} ${m.key.padEnd(16)} ${m.value}   ← ${m.from}`);
  }
  if (plan.needsValue.length) {
    console.log(`\n  needs a value (oneop won't invent these — fill from .env.example / 1Password):`);
    for (const k of plan.needsValue) console.log(`    • ${k}`);
  }

  if (flags.write) {
    const block = renderEnvBlock(plan);
    if (!block) return console.log(`\n✓ nothing to add — .env already has every managed key.`);
    if (existing) copyFileSync(envPath, envPath + ".bak");
    writeFileSync(envPath, existing + block, "utf8");
    console.log(`\n✓ wrote ${plan.managed.filter((m) => m.status === "new").length} key(s)${existing ? " (backed up .env → .env.bak)" : ""}.`);
  } else {
    console.log(`\n(dry run — re-run with --write to apply, merge-safe)`);
  }
}

async function cmdRun(name: string, flags: Flags) {
  const pb = await findApp(name);
  const env = envName(flags);
  const start = pb.envs[env]?.start;
  if (!start) die(`${name}/${env} has no "start" command in its playbook`);
  const cwd = repoRoot(pb);

  console.error(`oneop: running "${start}" in ${cwd}  (logs → ${logPath(name)})`);
  const child = spawn(start, { cwd, shell: true, stdio: ["inherit", "pipe", "pipe"] });

  const pipe = (stream: NodeJS.ReadableStream, which: "out" | "err") => {
    const rl = readline.createInterface({ input: stream });
    rl.on("line", (line) => {
      const level: Level = detectLevel(line, which);
      appendLogSync(name, { ts: Date.now(), level, line, stream: which });
      // pass through so the human still sees normal output
      (which === "err" ? process.stderr : process.stdout).write(line + "\n");
    });
  };
  pipe(child.stdout!, "out");
  pipe(child.stderr!, "err");

  child.on("exit", (code) => process.exit(code ?? 0));
  process.on("SIGINT", () => child.kill("SIGINT"));
}

async function cmdLogs(name: string, flags: Flags) {
  const level: Level | undefined = flags.errors ? "error" : (flags.level as Level) || undefined;
  const sinceMs = parseSince(flags.since as string);
  const lines = flags.lines ? Number(flags.lines) : undefined;
  const json = !!flags.json;

  const render = (entries: Awaited<ReturnType<typeof readLogs>>) => {
    if (json) return out(entries);
    for (const e of entries) {
      const t = new Date(e.ts).toISOString().slice(11, 19);
      console.log(`${t} ${e.level.toUpperCase().padEnd(5)} ${e.line}`);
    }
  };

  const entries = await readLogs(name, { level, sinceMs, lines });
  render(entries);

  if (flags.follow && !json) {
    let lastTs = entries.length ? entries[entries.length - 1].ts : sinceMs ?? Date.now();
    setInterval(async () => {
      const fresh = (await readLogs(name, { level })).filter((e) => e.ts > lastTs);
      if (fresh.length) {
        lastTs = fresh[fresh.length - 1].ts;
        render(fresh);
      }
    }, 700);
  }
}

async function main() {
  const { cmd, positional, flags } = parseArgs(process.argv.slice(2));
  switch (cmd) {
    case "init":
      return cmdInit(positional[0], flags);
    case "list":
      return cmdList();
    case "creds":
      return cmdCreds(positional[0] ?? die("usage: oneop creds <app>"), flags);
    case "env":
      return cmdEnv(positional[0] ?? die("usage: oneop env <app> [--write]"), flags);
    case "run":
      return cmdRun(positional[0] ?? die("usage: oneop run <app>"), flags);
    case "logs":
      return cmdLogs(positional[0] ?? die("usage: oneop logs <app>"), flags);
    case "help":
    case undefined:
      return console.log(HELP);
    default:
      die(`unknown command "${cmd}". Try: oneop help`);
  }
}

main().catch((e) => die(e instanceof Error ? e.message : String(e)));
